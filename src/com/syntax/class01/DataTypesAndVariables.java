package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		
		//Type name = assign value
		//Primitive data types
		//Numeric
		
		byte variable1=4;//range numbers -128 to 127
		
		short variable2=100;//Range -32768 to 32767

		int anyName=1000;//long number and is our Favorite. 
		
		long variable3=100000;//very long number
		
		//to store floating number
		
		float dec1=12.99f;
		double dec2=99.9999;
		
		//True and False or Yes or No
		
		boolean myVariable=true;
		boolean herVariable=false;
		
		
		//to represent single character
		
		char myFirstName=('A');
		char mySurname=('W');
		char myLastName=('Q');
		
		System.out.println(variable1);
		System.out.println(variable2);
		System.out.println(anyName);
		System.out.println(variable3);
		System.out.println(dec1);
		System.out.println(dec2);
		System.out.println(myVariable);
		System.out.println(herVariable);
		System.out.println(myFirstName);
		System.out.println(mySurname);
		System.out.println(myLastName);
		
	}

}
