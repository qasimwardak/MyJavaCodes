package com.syntax.class02;

public class VariableDeclaration {

	public static void main(String[] args) {
		
		//first way to create a variable
				//Create variable = ass
				//declare a variable=initialing
				double $num=12.99;
				
				System.out.println($num);
				
				//2nd way to create  a variable
				//declare a variable 
				//initialize or assigning value
				
				//we declare variable only once
				int myBox;
				myBox=135;//assigning value
				System.out.println(myBox);
				
				myBox=155;//reassigning value
				System.out.println(myBox);
				
				//3rd way
				
				int num1, num2, num3;//if the variable are the same type we can delcare them in one line. 
				num1=10;
				num2=20;
				num3=30;
				
				System.out.println(num1);
				System.out.println(num2);
				System.out.println(num3);
				
				//what will be the output
				
				char char1='C';
				char char2='D';
				System.out.println(char2);
				char char3=char1;
				System.out.println(char3);
				
				
				

	}

}
