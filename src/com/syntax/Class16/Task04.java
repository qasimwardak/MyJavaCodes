package com.syntax.Class16;

public class Task04 {
    public static void evenOrOddNumber (double number) {
        if(number%2==0) {
            System.out.println(number + " is even number");
        }else if (number%2!=0) {
            System.out.println(number + " is an Odd number");
        }else {
            System.out.println(number+" is Zero");
        }
    }

}
